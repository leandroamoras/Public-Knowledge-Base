#Vetores: São uma sequencia de elementos do mesmo tipo de uma só dimensão.
x= c(1,3,5,6,8) #criando um vetor

#Operações com vetores:

x+2
x/2
x*2

#media: Tira a media aritimetica do vetor.
mean(x)

#mediana: Tira a mediana
median(x)

#soma: toma todos os valores do vetor.
sum(x)

#desvio padrão:
sd(x)

#boxplot
boxplot(x)
